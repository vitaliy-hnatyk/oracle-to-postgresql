def dict_rows(cursor) -> list[dict[str, Any]]:
    names = [d[0] for d in cursor.description]
    return [dict(zip(names, row)) for row in cursor.fetchall()]


def fetch_tables(ora_conn, owner: str, selected_tables: list[str] | None) -> list[str]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT table_name
            FROM all_tables
            WHERE owner = :owner
              AND nested = 'NO'
              AND secondary = 'N'
              AND dropped = 'NO'
            ORDER BY table_name
            """,
            owner=owner,
        )
        tables = [row[0] for row in cur.fetchall()]
    if selected_tables:
        wanted = {t.upper() for t in selected_tables}
        tables = [t for t in tables if t.upper() in wanted]
    return tables


def fetch_columns(ora_conn, owner: str, table: str) -> list[dict[str, Any]]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT
                column_id,
                column_name,
                data_type,
                data_precision,
                data_scale,
                char_col_decl_length,
                data_length,
                nullable,
                identity_column
            FROM all_tab_columns
            WHERE owner = :owner
              AND table_name = :table_name
            ORDER BY column_id
            """,
            owner=owner,
            table_name=table,
        )
        return dict_rows(cur)


def fetch_primary_key_columns(ora_conn, owner: str, table: str) -> list[str]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT cols.column_name
            FROM all_constraints cons
            JOIN all_cons_columns cols
              ON cons.owner = cols.owner
             AND cons.constraint_name = cols.constraint_name
             AND cons.table_name = cols.table_name
            WHERE cons.owner = :owner
              AND cons.table_name = :table_name
              AND cons.constraint_type = 'P'
            ORDER BY cols.position
            """,
            owner=owner,
            table_name=table,
        )
        return [row[0] for row in cur.fetchall()]


def fetch_partition_info(ora_conn, owner: str, table: str) -> dict[str, Any] | None:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT partitioning_type, subpartitioning_type, partition_count
            FROM all_part_tables
            WHERE owner = :owner AND table_name = :table_name
            """,
            owner=owner,
            table_name=table,
        )
        row = cur.fetchone()
        if not row:
            return None
        info = {
            "partitioning_type": row[0],
            "subpartitioning_type": row[1],
            "partition_count": row[2],
        }

    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT column_name
            FROM all_part_key_columns
            WHERE owner = :owner AND name = :table_name AND object_type = 'TABLE'
            ORDER BY column_position
            """,
            owner=owner,
            table_name=table,
        )
        info["key_columns"] = [row[0] for row in cur.fetchall()]

    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT partition_name, partition_position, high_value
            FROM all_tab_partitions
            WHERE table_owner = :owner AND table_name = :table_name
            ORDER BY partition_position
            """,
            owner=owner,
            table_name=table,
        )
        parts = []
        for name, position, high_value in cur.fetchall():
            parts.append({"partition_name": name, "partition_position": position, "high_value": read_lob(high_value)})
        info["partitions"] = parts

    return info


def fetch_foreign_keys(ora_conn, owner: str) -> list[dict[str, Any]]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT
                c.constraint_name,
                c.table_name,
                c.r_constraint_name,
                c.delete_rule,
                r.table_name AS ref_table_name,
                cc.column_name,
                cc.position,
                rcc.column_name AS ref_column_name
            FROM all_constraints c
            JOIN all_constraints r
              ON r.owner = c.r_owner
             AND r.constraint_name = c.r_constraint_name
            JOIN all_cons_columns cc
              ON cc.owner = c.owner
             AND cc.constraint_name = c.constraint_name
             AND cc.table_name = c.table_name
            JOIN all_cons_columns rcc
              ON rcc.owner = r.owner
             AND rcc.constraint_name = r.constraint_name
             AND rcc.position = cc.position
            WHERE c.owner = :owner
              AND c.constraint_type = 'R'
            ORDER BY c.table_name, c.constraint_name, cc.position
            """,
            owner=owner,
        )
        return dict_rows(cur)


def fetch_indexes(ora_conn, owner: str) -> list[dict[str, Any]]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT
                i.index_name,
                i.table_name,
                i.uniqueness,
                i.index_type,
                i.generated,
                i.status,
                ic.column_name,
                ic.column_position,
                ic.descend
            FROM all_indexes i
            JOIN all_ind_columns ic
              ON ic.index_owner = i.owner
             AND ic.index_name = i.index_name
            WHERE i.owner = :owner
              AND i.index_type NOT LIKE 'LOB%'
            ORDER BY i.table_name, i.index_name, ic.column_position
            """,
            owner=owner,
        )
        rows = dict_rows(cur)

    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT index_name
            FROM all_constraints
            WHERE owner = :owner
              AND constraint_type IN ('P','U')
              AND index_name IS NOT NULL
            """,
            owner=owner,
        )
        constraint_indexes = {row[0] for row in cur.fetchall()}

    return [r for r in rows if r["INDEX_NAME"] not in constraint_indexes]


def fetch_views(ora_conn, owner: str) -> list[dict[str, Any]]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT view_name, text
            FROM all_views
            WHERE owner = :owner
            ORDER BY view_name
            """,
            owner=owner,
        )
        rows = []
        for view_name, text in cur.fetchall():
            rows.append({"VIEW_NAME": view_name, "TEXT": read_lob(text)})
        return rows


def fetch_synonyms(ora_conn, owner: str) -> list[dict[str, Any]]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT synonym_name, table_owner, table_name, db_link
            FROM all_synonyms
            WHERE owner = :owner
            ORDER BY synonym_name
            """,
            owner=owner,
        )
        return dict_rows(cur)


def fetch_triggers(ora_conn, owner: str) -> list[dict[str, Any]]:
    with ora_conn.cursor() as cur:
        cur.execute(
            """
            SELECT
                trigger_name,
                table_name,
                trigger_type,
                triggering_event,
                when_clause,
                trigger_body,
                status
            FROM all_triggers
            WHERE owner = :owner
            ORDER BY trigger_name
            """,
            owner=owner,
        )
        rows = []
        for row in cur.fetchall():
            rows.append(
                {
                    "TRIGGER_NAME": row[0],
                    "TABLE_NAME": row[1],
                    "TRIGGER_TYPE": row[2],
                    "TRIGGERING_EVENT": row[3],
                    "WHEN_CLAUSE": read_lob(row[4]),
                    "TRIGGER_BODY": read_lob(row[5]),
                    "STATUS": row[6],
                }
            )
        return rows


def fetch_source_grouped(ora_conn, owner: str, object_types: Iterable[str]) -> dict[tuple[str, str], str]:
    types = [t.upper() for t in object_types]
    binds = ",".join(f":t{i}" for i in range(len(types)))
    params = {"owner": owner, **{f"t{i}": t for i, t in enumerate(types)}}
    with ora_conn.cursor() as cur:
        cur.execute(
            f"""
            SELECT type, name, line, text
            FROM all_source
            WHERE owner = :owner
              AND type IN ({binds})
            ORDER BY type, name, line
            """,
            params,
        )
        grouped: dict[tuple[str, str], list[str]] = defaultdict(list)
        for typ, name, _line, text in cur.fetchall():
            grouped[(typ, name)].append(text)
    return {key: "".join(lines) for key, lines in grouped.items()}


