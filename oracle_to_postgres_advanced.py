#!/usr/bin/env python3
"""
Custom Oracle -> PostgreSQL migrator and converter.

This is intentionally not Ora2Pg. It uses:
  - python-oracledb to read Oracle metadata and table data
  - psycopg 3 to create PostgreSQL objects and stream data with COPY

Generated object support:
  - tables
  - table data
  - views
  - indexes
  - foreign keys
  - simple synonym replacements as PostgreSQL views
  - basic range/list/hash partition DDL
  - best-effort PL/SQL procedure/function/package/trigger conversion files

Important limitation:
  PL/SQL packages, triggers, and complex procedures are not 1:1 compatible with PostgreSQL.
  This script writes converted PL/pgSQL to reviewable SQL files by default.
"""

from __future__ import annotations

import argparse
import re
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any, Iterable

import oracledb
import psycopg
from psycopg import sql


# -----------------------------------------------------------------------------
# Naming and type helpers
# -----------------------------------------------------------------------------


def quote_ident(name: str) -> str:
    return '"' + name.replace('"', '""') + '"'


def pg_name(name: str, preserve_case: bool = False) -> str:
    return name if preserve_case else name.lower()


def read_lob(value: Any) -> Any:
    if value is None:
        return None
    if hasattr(value, "read"):
        return value.read()
    return value


def normalize_copy_value(value: Any) -> Any:
    return read_lob(value)


def map_oracle_type_to_postgres(col: dict[str, Any] | str) -> str:
    if isinstance(col, str):
        data_type = col.upper()
        precision = None
        scale = None
        char_len = None
        identity = False
    else:
        data_type = str(col.get("DATA_TYPE") or col.get("data_type") or "").upper()
        precision = col.get("DATA_PRECISION") or col.get("data_precision")
        scale = col.get("DATA_SCALE") or col.get("data_scale")
        char_len = (
            col.get("CHAR_COL_DECL_LENGTH")
            or col.get("char_col_decl_length")
            or col.get("DATA_LENGTH")
            or col.get("data_length")
        )
        identity = str(col.get("IDENTITY_COLUMN") or "NO").upper() == "YES"

    if identity:
        return "bigint GENERATED BY DEFAULT AS IDENTITY"

    if data_type in {"VARCHAR2", "NVARCHAR2", "VARCHAR"}:
        return f"varchar({char_len})" if char_len else "text"
    if data_type in {"CHAR", "NCHAR"}:
        return f"char({char_len})" if char_len else "char"
    if data_type in {"CLOB", "NCLOB", "LONG"}:
        return "text"
    if data_type in {"BLOB", "RAW", "LONG RAW"}:
        return "bytea"
    if data_type == "DATE":
        return "timestamp(0)"
    if data_type.startswith("TIMESTAMP WITH TIME ZONE"):
        return "timestamp with time zone"
    if data_type.startswith("TIMESTAMP WITH LOCAL TIME ZONE"):
        return "timestamp with time zone"
    if data_type.startswith("TIMESTAMP"):
        return "timestamp"
    if data_type == "BINARY_FLOAT":
        return "real"
    if data_type in {"BINARY_DOUBLE", "FLOAT"}:
        return "double precision"
    if data_type in {"PLS_INTEGER", "BINARY_INTEGER"}:
        return "integer"
    if data_type == "BOOLEAN":
        return "boolean"
    if data_type in {"NUMBER", "DECIMAL", "NUMERIC", "INTEGER"}:
        if precision is None and scale is None:
            return "numeric"
        if scale in (None, 0):
            if precision is not None:
                try:
                    p = int(precision)
                    if p <= 4:
                        return "smallint"
                    if p <= 9:
                        return "integer"
                    if p <= 18:
                        return "bigint"
                except Exception:
                    pass
            return "numeric"
        if precision is not None and scale is not None:
            return f"numeric({precision},{scale})"
        return "numeric"
    if data_type == "XMLTYPE":
        return "xml"
    return "text"


