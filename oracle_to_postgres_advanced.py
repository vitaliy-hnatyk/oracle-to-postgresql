def strip_oracle_schema_prefix(sql_text: str, oracle_schema: str, preserve_case: bool) -> str:
    schema = re.escape(oracle_schema)
    replacement = pg_name(oracle_schema, preserve_case) + "."
    return re.sub(rf'"?{schema}"?\.', replacement, sql_text, flags=re.IGNORECASE)


def convert_oracle_expression(expr: str, oracle_schema: str = "", preserve_case: bool = False) -> str:
    """Best-effort Oracle SQL expression conversion."""
    if expr is None:
        return ""

    s = str(read_lob(expr)).strip()

    s = re.sub(r"\bEDITIONABLE\b", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\bNONEDITIONABLE\b", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\bFORCE\b", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\bNO FORCE\b", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\bFROM\s+DUAL\b", "", s, flags=re.IGNORECASE)

    replacements = [
        (r"\bSYSDATE\b", "CURRENT_TIMESTAMP"),
        (r"\bSYSTIMESTAMP\b", "CURRENT_TIMESTAMP"),
        (r"\bNVL\s*\(", "COALESCE("),
        (r"\bLENGTHB\s*\(", "length("),
        (r"\bSUBSTR\s*\(", "substring("),
        (r"\bINSTR\s*\(", "strpos("),
        (r"\bREGEXP_LIKE\s*\(([^,]+),\s*([^\)]+)\)", r"(\1 ~ \2)"),
    ]
    for pat, repl in replacements:
        s = re.sub(pat, repl, s, flags=re.IGNORECASE)

    s = re.sub(
        r"TO_DATE\s*\(\s*'([^']+)'\s*,\s*'[^']+'\s*\)",
        lambda m: "DATE '" + m.group(1).strip().split()[0] + "'",
        s,
        flags=re.IGNORECASE,
    )
    s = re.sub(
        r"TO_TIMESTAMP\s*\(\s*'([^']+)'\s*,\s*'[^']+'\s*\)",
        lambda m: "TIMESTAMP '" + m.group(1).strip() + "'",
        s,
        flags=re.IGNORECASE,
    )

    if "(+)" in s:
        s += "\n-- TODO: Oracle outer join operator (+) must be rewritten as LEFT/RIGHT JOIN."
    if re.search(r"\bDECODE\s*\(", s, flags=re.IGNORECASE):
        s += "\n-- TODO: DECODE() should be rewritten as CASE."
    if re.search(r"\bCONNECT\s+BY\b", s, flags=re.IGNORECASE):
        s += "\n-- TODO: CONNECT BY should be rewritten as WITH RECURSIVE."
    if re.search(r"\bPIVOT\b", s, flags=re.IGNORECASE):
        s += "\n-- TODO: Oracle PIVOT needs manual PostgreSQL rewrite."

    if oracle_schema:
        s = strip_oracle_schema_prefix(s, oracle_schema, preserve_case)

    return s


def convert_plsql_body(body: str, oracle_schema: str = "", preserve_case: bool = False) -> str:
    """Best-effort PL/SQL body conversion to PL/pgSQL-like code."""
    s = str(read_lob(body) or "")

    s = re.sub(r":NEW\.([A-Za-z0-9_$#]+)", r"NEW.\1", s, flags=re.IGNORECASE)
    s = re.sub(r":OLD\.([A-Za-z0-9_$#]+)", r"OLD.\1", s, flags=re.IGNORECASE)

    replacements = [
        (r"\bNVL\s*\(", "COALESCE("),
        (r"\bSYSDATE\b", "CURRENT_TIMESTAMP"),
        (r"\bSYSTIMESTAMP\b", "CURRENT_TIMESTAMP"),
        (r"\bTRUE\b", "TRUE"),
        (r"\bFALSE\b", "FALSE"),
    ]
    for pat, repl in replacements:
        s = re.sub(pat, repl, s, flags=re.IGNORECASE)

    # SQL%ROWCOUNT has no direct inline equivalent.
    s = re.sub(
        r"\bSQL%ROWCOUNT\b",
        "/* TODO: replace SQL%ROWCOUNT with GET DIAGNOSTICS var = ROW_COUNT */ 0",
        s,
        flags=re.IGNORECASE,
    )

    # RAISE_APPLICATION_ERROR(-20001, 'msg') -> RAISE EXCEPTION 'msg'
    s = re.sub(
        r"RAISE_APPLICATION_ERROR\s*\(\s*-?\d+\s*,\s*'([^']*)'\s*\)\s*;",
        r"RAISE EXCEPTION '\1';",
        s,
        flags=re.IGNORECASE,
    )

    s = re.sub(r"\n/\s*$", "", s.strip())

    if oracle_schema:
        s = strip_oracle_schema_prefix(s, oracle_schema, preserve_case)

    return s


def comment_block(text: str) -> str:
    return "\n".join("-- " + line for line in str(text).splitlines())


def split_package_subprograms(package_body_source: str) -> list[str]:
    """
    Simple package body splitter.
    It handles many normal package bodies, but nested routines can confuse it.
    """
    src = str(read_lob(package_body_source) or "")
    blocks: list[str] = []
    pattern = re.compile(
        r"\b(PROCEDURE|FUNCTION)\s+([A-Za-z0-9_$#]+)\b.*?\bEND\s+\2\s*;",
        flags=re.IGNORECASE | re.DOTALL,
    )
    for match in pattern.finditer(src):
        blocks.append(match.group(0))
    return blocks


def convert_routine_block(
    source: str,
    schema: str,
    routine_name: str | None = None,
    package_name: str | None = None,
    oracle_schema: str = "",
    preserve_case: bool = False,
) -> str:
    """Convert a PL/SQL procedure/function block into reviewable PL/pgSQL."""
    src = str(read_lob(source) or "").strip().rstrip("/").strip()
    src = convert_plsql_body(src, oracle_schema, preserve_case)

    header_match = re.search(
        r"\b(CREATE\s+OR\s+REPLACE\s+)?(EDITIONABLE\s+|NONEDITIONABLE\s+)?(PROCEDURE|FUNCTION)\s+([A-Za-z0-9_$#]+)",
        src,
        flags=re.IGNORECASE,
    )

    if not header_match:
        name = routine_name or "unknown_routine"
        return (
            f"-- TODO: Could not parse routine header for {name}. Manual conversion required.\n"
            f"-- Original source follows:\n"
            + comment_block(src)
            + "\n"
        )

    kind = header_match.group(3).upper()
    original_name = routine_name or header_match.group(4)
    final_name = pg_name(original_name, preserve_case)
    if package_name:
        final_name = pg_name(package_name, preserve_case) + "__" + final_name

    body_match = re.search(r"\b(IS|AS)\b(.*)$", src, flags=re.IGNORECASE | re.DOTALL)
    body_part = body_match.group(2).strip() if body_match else src
    body_part = re.sub(r"\bEND\s+[A-Za-z0-9_$#]+\s*;\s*$", "END;", body_part, flags=re.IGNORECASE | re.DOTALL)

    if not re.search(r"\bBEGIN\b", body_part, flags=re.IGNORECASE):
        body_part = "BEGIN\n    -- TODO: Add converted routine statements here.\nEND;"

    if kind == "FUNCTION":
        return_type = "text"
        ret = re.search(r"\bRETURN\s+([A-Za-z0-9_$.%]+)", src, flags=re.IGNORECASE)
        if ret:
            return_type = map_oracle_type_to_postgres(ret.group(1).split(".")[-1])

        return (
            f"CREATE OR REPLACE FUNCTION {quote_ident(schema)}.{quote_ident(final_name)}()\n"
            f"RETURNS {return_type}\n"
            f"LANGUAGE plpgsql\n"
            f"AS $$\n"
            f"{body_part}\n"
            f"$$;\n"
            f"-- TODO: Review parameters, return type, exceptions, cursors, package state, and transaction behavior.\n"
        )

    return (
        f"CREATE OR REPLACE PROCEDURE {quote_ident(schema)}.{quote_ident(final_name)}()\n"
        f"LANGUAGE plpgsql\n"
        f"AS $$\n"
        f"{body_part}\n"
        f"$$;\n"
        f"-- TODO: Review parameters, exceptions, cursors, package state, and transaction behavior.\n"
    )


