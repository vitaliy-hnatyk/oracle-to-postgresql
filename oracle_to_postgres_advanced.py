def create_schema_sql(pg_schema: str) -> str:
    return f"CREATE SCHEMA IF NOT EXISTS {quote_ident(pg_schema)};\n"


def table_ddl_sql(
    ora_conn,
    owner: str,
    table: str,
    pg_schema: str,
    preserve_case: bool,
    convert_partitions: bool,
) -> list[str]:
    columns = fetch_columns(ora_conn, owner, table)
    pk_cols = fetch_primary_key_columns(ora_conn, owner, table)
    pg_table = pg_name(table, preserve_case)

    column_defs: list[str] = []
    for col in columns:
        source_col = col["COLUMN_NAME"]
        target_col = pg_name(source_col, preserve_case)
        pg_type = map_oracle_type_to_postgres(col)
        nullable = str(col["NULLABLE"]).upper() == "Y"
        not_null = "" if nullable else " NOT NULL"
        column_defs.append(f"    {quote_ident(target_col)} {pg_type}{not_null}")

    if pk_cols:
        pg_pk_cols = [quote_ident(pg_name(c, preserve_case)) for c in pk_cols]
        column_defs.append(f"    PRIMARY KEY ({', '.join(pg_pk_cols)})")

    partition_info = fetch_partition_info(ora_conn, owner, table) if convert_partitions else None
    partition_clause = ""
    partition_ddls: list[str] = []

    if partition_info:
        ptype = str(partition_info["partitioning_type"]).upper()
        key_cols = [quote_ident(pg_name(c, preserve_case)) for c in partition_info.get("key_columns", [])]
        if key_cols and ptype in {"RANGE", "LIST", "HASH"}:
            partition_clause = f" PARTITION BY {ptype} ({', '.join(key_cols)})"
            partition_ddls = partition_ddl_sql(partition_info, pg_schema, pg_table, preserve_case)
        else:
            partition_ddls.append(f"-- TODO: Unsupported partition type/key for {table}: {partition_info}\n")

    ddl = (
        f"CREATE TABLE {quote_ident(pg_schema)}.{quote_ident(pg_table)} (\n"
        + ",\n".join(column_defs)
        + f"\n){partition_clause};\n"
    )
    return [ddl] + partition_ddls


def partition_ddl_sql(info: dict[str, Any], pg_schema: str, pg_table: str, preserve_case: bool) -> list[str]:
    ptype = str(info["partitioning_type"]).upper()
    parts = info.get("partitions", [])
    ddls: list[str] = []

    if ptype == "RANGE":
        previous = "MINVALUE"
        for part in parts:
            part_name = pg_name(part["partition_name"], preserve_case)
            high = convert_partition_bound(part.get("high_value"))
            to_value = "MAXVALUE" if high.upper() == "MAXVALUE" else high
            ddls.append(
                f"CREATE TABLE {quote_ident(pg_schema)}.{quote_ident(pg_table + '_' + part_name)} "
                f"PARTITION OF {quote_ident(pg_schema)}.{quote_ident(pg_table)} "
                f"FOR VALUES FROM ({previous}) TO ({to_value});\n"
            )
            previous = to_value
        return ddls

    if ptype == "LIST":
        for part in parts:
            part_name = pg_name(part["partition_name"], preserve_case)
            high = convert_partition_bound(part.get("high_value"))
            ddls.append(
                f"CREATE TABLE {quote_ident(pg_schema)}.{quote_ident(pg_table + '_' + part_name)} "
                f"PARTITION OF {quote_ident(pg_schema)}.{quote_ident(pg_table)} "
                f"FOR VALUES IN ({high});\n"
            )
        return ddls

    if ptype == "HASH":
        modulus = max(len(parts), 1)
        for i, part in enumerate(parts):
            part_name = pg_name(part["partition_name"], preserve_case)
            ddls.append(
                f"CREATE TABLE {quote_ident(pg_schema)}.{quote_ident(pg_table + '_' + part_name)} "
                f"PARTITION OF {quote_ident(pg_schema)}.{quote_ident(pg_table)} "
                f"FOR VALUES WITH (MODULUS {modulus}, REMAINDER {i});\n"
            )
        return ddls

    return [f"-- TODO: Unsupported partitioning type: {ptype}\n"]


def convert_partition_bound(value: Any) -> str:
    s = str(read_lob(value) or "").strip()
    return convert_oracle_expression(s)


def foreign_key_sql(rows: list[dict[str, Any]], pg_schema: str, preserve_case: bool) -> list[str]:
    grouped: dict[tuple[str, str, str, str], list[dict[str, Any]]] = defaultdict(list)
    for r in rows:
        key = (r["TABLE_NAME"], r["CONSTRAINT_NAME"], r["REF_TABLE_NAME"], r["DELETE_RULE"])
        grouped[key].append(r)

    ddls: list[str] = []
    for (table, cname, ref_table, delete_rule), cols in grouped.items():
        cols = sorted(cols, key=lambda r: r["POSITION"])
        local_cols = ", ".join(quote_ident(pg_name(r["COLUMN_NAME"], preserve_case)) for r in cols)
        ref_cols = ", ".join(quote_ident(pg_name(r["REF_COLUMN_NAME"], preserve_case)) for r in cols)
        on_delete = ""
        if delete_rule and str(delete_rule).upper() not in {"NO ACTION", "RESTRICT"}:
            on_delete = f" ON DELETE {str(delete_rule).upper()}"
        ddls.append(
            f"ALTER TABLE {quote_ident(pg_schema)}.{quote_ident(pg_name(table, preserve_case))} "
            f"ADD CONSTRAINT {quote_ident(pg_name(cname, preserve_case))} "
            f"FOREIGN KEY ({local_cols}) "
            f"REFERENCES {quote_ident(pg_schema)}.{quote_ident(pg_name(ref_table, preserve_case))} ({ref_cols})"
            f"{on_delete};\n"
        )
    return ddls


def index_sql(rows: list[dict[str, Any]], pg_schema: str, preserve_case: bool) -> list[str]:
    grouped: dict[tuple[str, str, str, str], list[dict[str, Any]]] = defaultdict(list)
    for r in rows:
        key = (r["TABLE_NAME"], r["INDEX_NAME"], r["UNIQUENESS"], r["INDEX_TYPE"])
        grouped[key].append(r)

    ddls: list[str] = []
    for (table, idx, uniqueness, index_type), cols in grouped.items():
        cols = sorted(cols, key=lambda r: r["COLUMN_POSITION"])
        pg_cols = []
        for r in cols:
            col = quote_ident(pg_name(r["COLUMN_NAME"], preserve_case))
            if str(r.get("DESCEND") or "ASC").upper() == "DESC":
                col += " DESC"
            pg_cols.append(col)

        unique = "UNIQUE " if str(uniqueness).upper() == "UNIQUE" else ""
        note = ""
        if "BITMAP" in str(index_type).upper():
            note = "-- Oracle BITMAP index converted to normal PostgreSQL btree index. Review manually.\n"
        elif "FUNCTION" in str(index_type).upper():
            note = "-- TODO: Function-based index may need manual expression conversion.\n"

        ddls.append(
            note
            + f"CREATE {unique}INDEX {quote_ident(pg_name(idx, preserve_case))} "
            + f"ON {quote_ident(pg_schema)}.{quote_ident(pg_name(table, preserve_case))} "
            + f"({', '.join(pg_cols)});\n"
        )
    return ddls


def view_sql(rows: list[dict[str, Any]], pg_schema: str, oracle_schema: str, preserve_case: bool) -> list[str]:
    ddls: list[str] = []
    for r in rows:
        name = pg_name(r["VIEW_NAME"], preserve_case)
        body = convert_oracle_expression(r["TEXT"], oracle_schema, preserve_case)
        ddls.append(
            f"CREATE OR REPLACE VIEW {quote_ident(pg_schema)}.{quote_ident(name)} AS\n"
            f"{body}\n;\n"
            f"-- TODO: Review Oracle-specific functions, DECODE(), CONNECT BY, MODEL, PIVOT, and outer joins.\n"
        )
    return ddls


def synonym_sql(rows: list[dict[str, Any]], pg_schema: str, preserve_case: bool) -> list[str]:
    ddls: list[str] = []
    for r in rows:
        synonym = pg_name(r["SYNONYM_NAME"], preserve_case)
        target_schema = pg_name(r["TABLE_OWNER"], preserve_case)
        target_name = pg_name(r["TABLE_NAME"], preserve_case)
        db_link = r.get("DB_LINK")

        if db_link:
            ddls.append(
                f"-- TODO: Synonym {synonym} points through Oracle DB_LINK {db_link}; "
                f"PostgreSQL needs postgres_fdw, dblink, or manual replacement.\n"
            )
            continue

        ddls.append(
            f"CREATE OR REPLACE VIEW {quote_ident(pg_schema)}.{quote_ident(synonym)} AS\n"
            f"SELECT * FROM {quote_ident(target_schema)}.{quote_ident(target_name)};\n"
            f"-- Synonym replacement: Oracle synonym {synonym} -> view over {target_schema}.{target_name}.\n"
        )
    return ddls


def trigger_sql(rows: list[dict[str, Any]], pg_schema: str, oracle_schema: str, preserve_case: bool) -> list[str]:
    ddls: list[str] = []
    for r in rows:
        trg = pg_name(r["TRIGGER_NAME"], preserve_case)
        table = pg_name(r["TABLE_NAME"], preserve_case)
        fn = f"{trg}_fn"
        trigger_type = str(r["TRIGGER_TYPE"] or "").upper()
        triggering_event = str(r["TRIGGERING_EVENT"] or "").upper()
        body = convert_plsql_body(r["TRIGGER_BODY"], oracle_schema, preserve_case)

        timing = "BEFORE"
        if "AFTER" in trigger_type:
            timing = "AFTER"
        elif "INSTEAD OF" in trigger_type:
            timing = "INSTEAD OF"

        level = "FOR EACH ROW" if "EACH ROW" in trigger_type else "FOR EACH STATEMENT"
        events = " OR ".join(e.strip() for e in triggering_event.split(" OR ") if e.strip())
        events = events or "INSERT OR UPDATE OR DELETE"

        when_clause = ""
        if r.get("WHEN_CLAUSE"):
            when_expr = convert_plsql_body(r["WHEN_CLAUSE"], oracle_schema, preserve_case)
            when_clause = f"WHEN ({when_expr})\n"

        return_stmt = "RETURN NEW;"
        if "DELETE" in triggering_event and "INSERT" not in triggering_event and "UPDATE" not in triggering_event:
            return_stmt = "RETURN OLD;"
        if "STATEMENT" in level:
            return_stmt = "RETURN NULL;"

        ddls.append(
            f"CREATE OR REPLACE FUNCTION {quote_ident(pg_schema)}.{quote_ident(fn)}()\n"
            f"RETURNS trigger\n"
            f"LANGUAGE plpgsql\n"
            f"AS $$\n"
            f"BEGIN\n"
            f"{body}\n"
            f"    {return_stmt}\n"
            f"END;\n"
            f"$$;\n\n"
            f"DROP TRIGGER IF EXISTS {quote_ident(trg)} ON {quote_ident(pg_schema)}.{quote_ident(table)};\n"
            f"CREATE TRIGGER {quote_ident(trg)}\n"
            f"{timing} {events} ON {quote_ident(pg_schema)}.{quote_ident(table)}\n"
            f"{level}\n"
            f"{when_clause}"
            f"EXECUTE FUNCTION {quote_ident(pg_schema)}.{quote_ident(fn)}();\n"
            f"-- TODO: Review trigger transition variables, mutating-table logic, WHEN clause, autonomous transactions, and statement-level behavior.\n"
        )
    return ddls


def routine_sql(source_map: dict[tuple[str, str], str], pg_schema: str, oracle_schema: str, preserve_case: bool) -> list[str]:
    ddls: list[str] = []
    for (typ, name), source in sorted(source_map.items()):
        if typ in {"PROCEDURE", "FUNCTION"}:
            ddls.append(convert_routine_block(source, schema=pg_schema, routine_name=name, oracle_schema=oracle_schema, preserve_case=preserve_case))
    return ddls


def package_sql(source_map: dict[tuple[str, str], str], pg_schema: str, oracle_schema: str, preserve_case: bool) -> list[str]:
    ddls: list[str] = []
    for (typ, package_name), source in sorted(source_map.items()):
        if typ != "PACKAGE BODY":
            continue
        blocks = split_package_subprograms(source)
        if not blocks:
            ddls.append(f"-- TODO: Could not split package body {package_name}. Manual conversion required.\n" + comment_block(source) + "\n")
            continue
        ddls.append(f"-- Package {package_name} converted into standalone routines named package__routine.\n")
        ddls.append("-- TODO: PostgreSQL has no Oracle-style package state. Package variables/constants/types must be redesigned manually.\n")
        for block in blocks:
            name_match = re.search(r"\b(PROCEDURE|FUNCTION)\s+([A-Za-z0-9_$#]+)", block, flags=re.IGNORECASE)
            routine_name = name_match.group(2) if name_match else None
            ddls.append(convert_routine_block(block, schema=pg_schema, routine_name=routine_name, package_name=package_name, oracle_schema=oracle_schema, preserve_case=preserve_case))
    return ddls


